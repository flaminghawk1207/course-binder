import { type NextPage } from "next";
import Head from "next/head";
import { useContext, useEffect, useState } from "react";
import { UserContext } from "./_app";
import { userSetter } from "~/types/internal";
import { useRouter } from "next/router";

const Home: NextPage<{setUser: userSetter}> = ({setUser}) => {
  const user = useContext(UserContext);

  const router = useRouter();


  // Show login screen if user is not defined
  useEffect(() => {
    if(!user) router.push("/login");
  })
  useEffect(() => {
    if(!user) {
      router.push("/index");
    } else if (user.role === "faculty") {
      router.push("/FacultyDisplayPage");
    }
  }, [user])
  // Main App UI
  
  return (
    <>
      <Head>
        <title>Course Binder</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center">
        <div className="container flex flex-col items-center justify-center gap-12 px-4 py-16 ">
          Hello {user?.name}!
          <button onClick={() => setUser(null)}>Logout</button>
          <SampleChild/>
        </div>

      </main>

    </>
  );
};

export default Home;

const SampleChild = () => {
  const user = useContext(UserContext);
  return <div>Hello {user?.name}! from child. You are a {user?.role}.</div>
}
